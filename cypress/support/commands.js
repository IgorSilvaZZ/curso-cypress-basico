Cypress.Commands.add("fillMandatoryFieldsAndSubmit", (dataForm) => {
  cy.get("input#firstName").type(dataForm.firstName);
  cy.get("input#lastName").type(dataForm.lastName);
  cy.get("input#email").type(dataForm.email);
  cy.get("textarea#open-text-area").type(
    "Lorem Ipsum is simply dummy text of the printing and typesetting industry. Lorem Ipsum has been the industry's standard dummy text ever since the 1500s, when an unknown printer took a galley of type and scrambled it to make a type specimen book.",
    {
      delay: 0,
    }
  );

  cy.contains("button", "Enviar").click();
});
